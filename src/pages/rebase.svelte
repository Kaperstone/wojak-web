# Rebase token<br>
<br>
Rebase on $BNB called `BEAN`<br>
<br>
Stake WBNB into the pool, get MUMU in exchange.<br>
<br>
<br>
WBNB in the pool will generate profit, the profit will be used to buyback `BEAN`s from the market<br>
<br>
<br>
When under peg, BEAN owners will be able to exchange BEAN for `BOBO` tokens.<br>
The more BEAN is under peg, the more BOBO will be given.<br>
After BEAN is above 1 BNB, each BOBO will be exchange for 1:1 BOBO to BEAN tokens.<br>
<br>
When BEAN is above peg, BOBO will open to be claimed and BEAN's that were bought back will start to be distributed<br>
at the rate relative to the percentage above peg.<br>
At the same period, the token will shift the profits from the vault to fill the treasury.<br>
<br>
<br>
The treasury is perm. and will participate infinitely in the vault.<br>
<br>
<br>
<br>
This kind of the design will flip the cards, unlike Tomb Finance, where people race to redeem their TBOND
at the highest price while the protocol tries to push down the price when above peg.<br>
<br>
Participants of this design, will race to redeem as many tokens at the lowest price, while the protocol tries
to push the price up.